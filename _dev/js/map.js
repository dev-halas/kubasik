function initFooterMap(){
    const map = new google.maps.Map(document.getElementById("map"), configMap)
    const contentString =
        '<div id="mapContent">' +
        'Kubasik INOX' + '<br>' +
        'Miłochowice 20A, ' + '<br>' +
        '56-300 Miłochowice' +
        "</div>";
    
    const infowindow = new google.maps.InfoWindow({
        content: contentString,
        maxWidth: 300,
    });
    
    const icon = {
        path: "M65.9,0A31.765,31.765,0,0,0,43.443,54.23L65.9,76.247,88.372,54.226A31.766,31.766,0,0,0,65.9,0Zm0,47.654A15.885,15.885,0,1,1,81.789,31.769,15.884,15.884,0,0,1,65.9,47.654Z",
        fillColor: '#e62a28',
        strokeWeight: 0,
        fillOpacity: 1, 
        anchor: new google.maps.Point(64, 78),
    };
    
    const marker = new google.maps.Marker({
        position: pinMap,
        map,
        icon: icon,
        title: "Kubasik",
    });
    
    marker.addListener("click", () => {
        infowindow.open({
          anchor: marker,
          map,
          shouldFocus: false,
        });
    });
}

